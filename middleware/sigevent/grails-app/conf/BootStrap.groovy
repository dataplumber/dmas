   private void populate(def map) {
      map.CATEGORIES.each {category ->
         def group = SysEventGroup.findByCategory(category)
         if(!group) {
            log.debug("Adding "+category)
            
            List types = map.TYPES.containsKey(category) ? map.TYPES.get(category) : map.DEFAULT_TYPES
            long purgeRate = map.PURGE_RATES.containsKey(category) ? map.PURGE_RATES.get(category) : map.DEFAULT_PURGE_RATE
            
            types.each {type ->
               group = new SysEventGroup("type": type, "category": category, "purgeRate": purgeRate)
               if(!group.save(flush: true)) {
                  log.debug("Failed to populate EventGroup: "+group.errors.allErrors.join())
               }
            }
         }
      }
      
      map.ROLES.each {entry ->
         def role = SysRole.findByName(entry.name)
         if(!role) {
            log.info("Adding role: "+entry.name)
            
            int admin = entry.admin ? 1 : 0
            role = new SysRole("name": entry.name, "admin": admin)
            if(!role.save(flush: true)) {
               log.error("Failed to populate Role: "+role.errors.allErrors.join())
            }
         }
      }
      
      map.USERS.each {entry ->
         def user = SysUser.findByUsername(entry.username)
         if(!user) {
            def role = SysRole.findByName(entry.role)
            if(!role) {
               log.info("Role is missing, skipping: "+entry.role)
            } else {
               log.info("Adding user: "+entry.username)
               
               def password = DigestUtility.getDigest("SHA-1", entry.password)
               user = new SysUser(
                  "username": entry.username,
                  "password": password.toLowerCase(),
                  "role": role
               )
               if(!user.save(flush: true)) {
                  log.error("Failed to populate User: "+user.errors.allErrors.join())
               }
            }
         }
      }
   }
